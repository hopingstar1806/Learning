# Changing and adding Dictionary Elements
my_dict = {'name': 'Nguyen Khang', 'age': 17}

# update value
my_dict['age'] = 18

#Output: {'name': 'Nguyen Khang', 'age': 18}
print(my_dict)

# add item
my_dict['address'] = 'Ho Chi Minh'

# Output: {'name': 'Nguyen Khang', 'age': 18, 'address': 'Ho Chi Minh'}
print(my_dict)
